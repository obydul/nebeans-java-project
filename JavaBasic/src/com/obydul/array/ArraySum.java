package com.obydul.array;

public class ArraySum {
    public static void main(String[] args) {
        int[] myArray = {10, 20, 30, 50};
        int sum = 0;
        for (int x = 0; x <= myArray.length; x++) {
            sum += myArray[x];
        }
        System.out.println(sum);
        
        int x = 2;
        System.out.println(5/x);
        
    }

}
