    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String txtUser = txtUserName.getText();
        String txtpass = txtPassword.getText();
        try {
            PreparedStatement stm = dbca.getConn().prepareStatement("SELECT username, password FROM USER2 WHERE username=? AND password=?");
            stm.setString(1, txtUser);
            stm.setString(2, txtpass);
            ResultSet rs = stm.executeQuery();
            if(rs.next()){
                lblMessage.setText("Login successful");
                //lblMessage.setBackground(Color.green);
                lblMessage.setForeground(Color.green);
                stm.close();
                rs.close();
            }
            else {
                lblMessage.setText("Login unsuccessful");
                //lblMessage.setBackground(Color.red);
                lblMessage.setForeground(Color.red);
                stm.close();
                rs.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginInDB.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

