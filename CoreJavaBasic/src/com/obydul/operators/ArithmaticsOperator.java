package com.obydul.operators;

public class ArithmaticsOperator {

    public static void main(String[] args) {
        int n, nm, result;
        n = 15;
        nm = 6;
        result = n + nm;
        System.out.println(result);
        result = n - nm;
        System.out.println(result);
        result = n * nm;
        System.out.println(result);
        result = n / nm;
        System.out.println(result);
        result = n % nm;
        System.out.println(result);
        
       
    }
}
