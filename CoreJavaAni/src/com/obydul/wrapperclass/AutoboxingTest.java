package com.obydul.wrapperclass;

public class AutoboxingTest {
    public static void main(String[] args) {
        // convert primitive to object
        int a = 10;
        Integer b = a;
        System.out.println(b);
        char c = 'b';
        Character ch = c;
        System.out.println(ch);
        
    }
    
}
