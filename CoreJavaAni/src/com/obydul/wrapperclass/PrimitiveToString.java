package com.obydul.wrapperclass;

public class PrimitiveToString {

    public static void main(String[] args) {
        int a = 100;
        String s = Integer.toString(a);
        System.out.println(s);

        double d = 1.5;
        String ds = Double.toString(d);
        System.out.println(ds);
    }
}
